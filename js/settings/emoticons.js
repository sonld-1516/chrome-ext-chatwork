function storageEmoticons() {
  let emos = [];

  emoticon_settings.forEach(emoSetting => {
    // console.log('emoSetting', emoSetting);
    let data_name = emoSetting.data_name;
    let priority = 0;
    let _emoticons = emoSetting.emoticons;

    for (let i = 0; i < _emoticons.length; i++) {
      _emoticons[i].data_name = data_name;
      _emoticons[i].priority = priority;
      emos.push(_emoticons[i]);
    }
  });

  localStorage['CWPLUS_EMOTICONS_DATA'] = JSON.stringify(emos);
}

storageEmoticons();
